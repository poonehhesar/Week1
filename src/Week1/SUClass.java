package Week1;

public class SUClass {

	// 1
	public long minL(long num1, long num2) {
		return Math.min(num1, num2);
	}

	// 2
	public float minF(float num1, float num2) {
		return Math.min(num1, num2);
	}

	// 3
	public int minI(int num1, int num2) {
		return Math.min(num1, num2);
	}

	// 4
	public double minD(double num1, double num2) {
		return Math.min(num1, num2);
	}

	// 5
	public int maxI(int num1, int num2) {
		return Math.max(num1, num2);
	}

	// 6
	public double maxD(double num1, double num2) {
		return Math.max(num1, num2);
	}

	// 7
	public int maxI2(int num1, int num2) {
		return Math.max(num1, num2);
	}
	
	// 8
	public long maxL(long num1, long num2) {
		return Math.max(num1, num2);
	}

	// 9
	public double pow(double num1, double num2) {
		return Math.pow(num1, num2);
	}

	// 10
	public int addNum(int num) {
		int sum = 1 + num;
		return sum;
	}

	// 11
	public long addNumL(long num) {
		long sum = 1 + num;
		return sum;
	}

	// 12
	public int subNum(int num) {
		int diff = num - 1;
		return diff;
	}

	// 13
	public long subNumL(long num) {
		long diff = num - 1;
		return diff;
	}

	// 14
	public double SRDouble(double num) {
		return Math.sqrt(num);
	}

	// 15
	public double CRDouble(double num) {
		return Math.cbrt(num);
	}

}
